class ConfigObject(SimpleNamespace):
    """Enhanced configuration object with serialization support"""
    def __init__(self, **kwargs):
        super().__init__(**kwargs)
        
    def __getattr__(self, name):
        return None
    
    def to_dict(self):
        """Recursively convert to nested dictionary"""
        return {
            k: v.to_dict() if isinstance(v, ConfigObject) else v
            for k, v in self.__dict__.items()
        }

def load_config(config_path='./configs/config.yaml') -> ConfigObject:
    """
    Load configuration with object-style attribute access and environment overrides
    
    Example usage:
        config = load_config()
        print(config.data.dataset)  # instead of config['data']['dataset']
    """
    with open(config_path, 'r') as file:
        config_dict = yaml.safe_load(file) or {}

    def dict_to_obj(d):
        if isinstance(d, dict):
            return ConfigObject(**{k: dict_to_obj(v) for k, v in d.items()})
        return d

    config = dict_to_obj(config_dict)

    if dataset_name := os.getenv('DATASET_NAME'):
        config.data.dataset = dataset_name

    return config

def param_groups_lrd(model, weight_decay=0.05, no_weight_decay_list=[], layer_decay=0.75):
    param_group_names = {}
    param_groups = {}

    num_layers = len(model.blocks) + 1

    layer_scales = list(layer_decay ** (num_layers - i) for i in range(num_layers + 1))

    for n, p in model.named_parameters():
        if not p.requires_grad:
            continue

        # no decay: all 1D parameters and model specific ones
        if p.ndim == 1 or n in no_weight_decay_list:
            g_decay = "no_decay"
            this_decay = 0.
        else:
            g_decay = "decay"
            this_decay = weight_decay

        layer_id = get_layer_id_for_vit(n, num_layers)
        group_name = "layer_%d_%s" % (layer_id, g_decay)

        if group_name not in param_group_names:
            this_scale = layer_scales[layer_id]

            param_group_names[group_name] = {
                "lr_scale": this_scale,
                "weight_decay": this_decay,
                "params": [],
            }
            param_groups[group_name] = {
                "lr_scale": this_scale,
                "weight_decay": this_decay,
                "params": [],
            }

        param_group_names[group_name]["params"].append(n)
        param_groups[group_name]["params"].append(p)

    return list(param_groups.values())

def get_layer_id_for_vit(name, num_layers):
    if name in ['cls_token', 'pos_embed']:
        return 0
    elif name.startswith('patch_embed'):
        return 0
    elif name.startswith('blocks'):
        return int(name.split('.')[1]) + 1
    else:
        return num_layers

def initialize_mixup(config):
    mixup_fn = None
    mixup_active = config.data.augment.mixup_alpha > 0
    if mixup_active:
        print("Mixup is activated!")
        mixup_fn = Mixup(
            mixup_alpha=config.data.augment.mixup_alpha, prob=config.data.augment.mixup_prob, 
            mode=config.data.augment.mixup_mode, label_smoothing=config.hparams.optimizer.smoothing, 
            num_classes=config.data.num_classes)
    return mixup_fn

class RandomResizedCrop(transforms.RandomResizedCrop):
    """
    RandomResizedCrop for matching TF/TPU implementation: no for-loop is used.
    This may lead to results different with torchvision's version.
    Following BYOL's TF code:
    https://github.com/deepmind/deepmind-research/blob/master/byol/utils/dataset.py#L206
    """
    @staticmethod
    def get_params(img, scale, ratio):
        width, height = F.get_image_size(img)
        area = height * width

        target_area = area * torch.empty(1).uniform_(scale[0], scale[1]).item()
        log_ratio = torch.log(torch.tensor(ratio))
        aspect_ratio = torch.exp(
            torch.empty(1).uniform_(log_ratio[0], log_ratio[1])
        ).item()

        w = int(round(math.sqrt(target_area * aspect_ratio)))
        h = int(round(math.sqrt(target_area / aspect_ratio)))

        w = min(w, width)
        h = min(h, height)

        i = torch.randint(0, height - h + 1, size=(1,)).item()
        j = torch.randint(0, width - w + 1, size=(1,)).item()

        return i, j, h, w

